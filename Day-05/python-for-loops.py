# List
fruits = ["Apple", "Peach", "Pear"]

# For loop
for fruit in fruits:
  print(fruit)
  print(f'{fruit} pie')
print(fruits)


# For is a keyword that invokes the for loop
# 'fruit' is an identifier that denotes a single item in the list
# in is a keyword to look into the list
# Lastly, we enter the list we want to traverse

